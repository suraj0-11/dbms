CREATE TABLE DIRECTOR (
    DIR_ID NUMBER(3) PRIMARY KEY,
    DIR_NAME VARCHAR2(20),
    DIR_PHONE NUMBER(10)
);

CREATE TABLE ACTOR (
    ACT_ID NUMBER(3) PRIMARY KEY,
    ACT_NAME VARCHAR2(20),
    ACT_GENDER CHAR(1)
);

CREATE TABLE MOVIES (
    MOV_ID NUMBER(4) PRIMARY KEY,
    MOV_TITLE VARCHAR2(25),
    MOV_YEAR NUMBER(4),
    MOV_LANG VARCHAR2(12),
    DIR_ID NUMBER(3) REFERENCES DIRECTOR (DIR_ID) ON DELETE CASCADE
);

CREATE TABLE MOVIE_CAST (
    ACT_ID NUMBER(3) REFERENCES ACTOR (ACT_ID) ON DELETE CASCADE,
    MOV_ID NUMBER(4) REFERENCES MOVIES (MOV_ID) ON DELETE CASCADE,
    ROLE VARCHAR2(10),
    PRIMARY KEY (ACT_ID, MOV_ID)
);

CREATE TABLE RATING (
    MOV_ID NUMBER(4) REFERENCES MOVIES (MOV_ID) ON DELETE CASCADE,
    REV_STARS NUMBER(2)
);

DROP TABLE RATING;
DROP TABLE MOVIE_CAST;
DROP TABLE MOVIES;
